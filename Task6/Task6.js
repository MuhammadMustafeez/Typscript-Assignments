"use strict";
// Store the name with whitespace characters
let full_name = "\t\n Muhammad Mustafeez \t\n";
// Print the name with whitespace
console.log("Name with whitespace:", full_name);
// Strip the whitespace using the trim() method
let strip_fname = full_name.trim();
// Print the name after stripping whitespace
console.log("\nName after stripping whitespace:", strip_fname);
